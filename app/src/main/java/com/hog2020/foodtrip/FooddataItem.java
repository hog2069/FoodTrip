package com.hog2020.foodtrip;

public class FooddataItem {

    public int num;
    public String image;
    public String name;
    public String address;
    public String number;

    public FooddataItem() {
    }

    public FooddataItem(int num, String image, String name, String address, String number) {
        this.num = num;
        this.image = image;
        this.name = name;
        this.address = address;
        this.number = number;
    }
}
